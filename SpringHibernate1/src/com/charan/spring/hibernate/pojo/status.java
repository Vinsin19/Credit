package com.charan.spring.hibernate.pojo;

import java.util.*;

public class status {

	private int id;
	private String uid, app_id, app_dt, stat;

	public int getid() {
		return id;
	}

	public void setid(int id) {
		this.id = id;
	}
	
	public String getuid() {
		return uid;
	}

	public void setuid(String uid) {
		this.uid = uid;
	}

	public String getapp_id() {
		return app_id;
	}

	public void setapp_id(String app_id) {
		this.app_id = app_id;
	}

	public String getapp_dt() {
		return app_dt;
	}

	public void setapp_dt(String app_dt) {
		this.app_dt = app_dt;
	}

	public String getstat() {
		return stat;
	}

	public void setstat(String stat) {
		this.stat = stat;
	}
	
	
	
}
